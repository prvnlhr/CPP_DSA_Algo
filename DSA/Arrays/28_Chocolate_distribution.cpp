long long findMinDiff(vector<long long> arr, long long n, long long m)
{

    sort(arr.begin(), arr.end());

    long long minDiff = LONG_LONG_MAX;
    debug(arr);

    for (int i = 0; i <= n - m; i++)
    {
        minDiff = min(arr[i + m - 1] - arr[i], minDiff);
    }

    return minDiff;
}

