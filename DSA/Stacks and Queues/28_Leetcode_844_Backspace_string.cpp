string getString(string s)
{

    debug(s);
    stack<char> st;
    string res = "";
    for (int i = 0; i < s.size(); i++)
    {
        if (s[i] != '#')
        {
            st.push(s[i]);
        }
        else if (s[i] == '#' && !st.empty())
        {
            st.pop();
        }
    }
    while (!st.empty())
    {
        char ch = st.top();
        res = res + ch;
        debug(ch);
        st.pop();
    }
    return res;
}

