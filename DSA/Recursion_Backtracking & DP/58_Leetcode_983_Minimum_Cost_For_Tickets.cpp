int recursive(vector<int> &days, vector<int> &costs, int indx, int nextDay)
{
    if (indx >= days.size())
    {
        return 0;
    }
    if (days[indx] <= nextDay)
    {
        return recursive(days, costs, indx + 1, nextDay);
    }
    else
    {
        int cost1 = costs[0] + recursive(days, costs, indx + 1, days[indx]);
        int cost2 = costs[1] + recursive(days, costs, indx + 1, days[indx] + 6);
        int cost3 = costs[2] + recursive(days, costs, indx + 1, days[indx] + 29);
        return min({cost1, cost2, cost3});
    }
}
int recursiveMemo(vector<int> &days, vector<int> &costs, int indx, int nextDay, vector<vector<int>> &dp)
{
    if (indx >= days.size())
    {
        return 0;
    }
    if (dp[indx][nextDay] != -1)
    {
        return dp[indx][nextDay];
    }
    if (days[indx] <= nextDay)
    {
        return dp[indx][nextDay] = recursiveMemo(days, costs, indx + 1, nextDay, dp);
    }
    else
    {
        int cost1 = costs[0] + recursiveMemo(days, costs, indx + 1, days[indx], dp);
        int cost2 = costs[1] + recursiveMemo(days, costs, indx + 1, days[indx] + 6, dp);
        int cost3 = costs[2] + recursiveMemo(days, costs, indx + 1, days[indx] + 29, dp);
        dp[indx][nextDay] = min({cost1, cost2, cost3});
        return min({cost1, cost2, cost3});
    }
}
int mincostTickets(vector<int> &days, vector<int> &costs)
{
    int d = days.size();
    int max_days = days[d - 1] + 30;
    vector<vector<int>> dp(d + 1, vector<int>(max_days + 1, -1));
    return recursiveMemo(days, costs, 0, 0, dp);
    // return recursive(days, costs, 0, 0);
}
